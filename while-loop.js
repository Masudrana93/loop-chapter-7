//4. একটা while লুপ দিয়ে 1 থেকে 10 পর্যন্ত সংখ্যাগুলো প্রিন্ট করার সময় সংখ্যাগুলোকে 3 দিয়ে গুণ করে ফেলবি। তাহলে কিন্তু 3-এর নামতা হয়ে যাবে।

let printNum = 1;

while(printNum < 11){
    console.log(printNum * 3);
    printNum++;
}
//5. একটা while লুপ দিয়ে। 11 থেকে 110 পর্যন্ত সংখ্যাগুলো প্রিন্ট করার সময় সংখ্যাগুলোকে 2 দিয়ে ভাগ করে ফেলবি।

let numDivide = 11;

while(numDivide < 111){
    console.log(numDivide / 2);
    numDivide++;
}